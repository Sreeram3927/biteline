
class Food {
  final int id;
  final String name;
  final String imageUrl;
  final String type;
  final double price;
  final double rating;
  final String description;

  Food({
    required this.id,
    required this.name,
    required this.imageUrl,
    required this.type,
    required this.price,
    required this.rating,
    required this.description,
  });
}